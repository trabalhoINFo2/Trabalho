    private void Alterar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Alterar
        // TODO add your handling code here:
        //pega a linha selecionada
        int linha = TabelaP.getSelectedRow();
        
        //Pega o modelo que tem todos os dados da tabela
        TableModel model = TabelaP.getModel();
        
        
        String codstr = model.getValueAt(linha, 0).toString();
        int cod = Integer.parseInt(codstr);
        ProdutoDao pDao = new ProdutoDao();
        Produto p = null;
        try {
            p = pDao.consultarporcod(cod);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar cliente (cod-'" +cod+ "') no banco.-"  
                    +ex.getMessage());
            return;
        }
        if (p!=null){
            AlterarProdutoJFrame frame = new AlterarProdutoJFrame(p);
            frame.setVisible(true);
        }
    }//GEN-LAST:event_Alterar

