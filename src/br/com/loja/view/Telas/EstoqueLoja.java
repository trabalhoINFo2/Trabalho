    private void preencherTable(List<Produto> pList) {
        String[] cabecalho = new String[]{"CÃ³digo", "Nome", "Cor", "DataEntrada", 
            "Valor", "Fornecedor", "Quantidade"};

        String[][] conteudo = new String[pList.size()][7];
        for (int i = 0; i < pList.size(); i++) {
            Produto p = pList.get(i);
            conteudo[i][0] = p.getCod() + "";
            conteudo[i][1] = p.getTipo();
            conteudo[i][2] = p.getCor();
            conteudo[i][3] = p.getDataEntrada();
            conteudo[i][4] = p.getValor();
            conteudo[i][5] = p.getFornecedor();
            conteudo[i][6] = p.getQtd();
        }
        TabelaP.setModel(new javax.swing.table.DefaultTableModel(
                conteudo,
                cabecalho));
    }
